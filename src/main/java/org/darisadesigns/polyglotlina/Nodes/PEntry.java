package org.darisadesigns.polyglotlina.Nodes;

import java.util.Map;

/**
 *
 * @author draque.thompson
 * @param <K> Key Type
 * @param <V> Value Type
 */
public final class PEntry<K, V> implements Map.Entry<K, V> {
    private final K key;
    private V value;

    public PEntry(K _key, V _value) {
        this.key = _key;
        this.value = _value;
    }

    @Override
    public K getKey() {
        return key;
    }

    @Override
    public V getValue() {
        return value;
    }

    @Override
    public V setValue(V _value) {
        V old = this.value;
        this.value = _value;
        return old;
    }
    
    @Override
    public boolean equals(Object comp) {
        boolean ret = false;
        
        if (this == comp) {
            ret = true;
        } else if (comp != null && getClass() == comp.getClass()) {
            PEntry c = (PEntry)comp;
            ret = this.key.equals(c.key) && this.value.equals(c.value);
        }
        
        return ret;
    }
}
